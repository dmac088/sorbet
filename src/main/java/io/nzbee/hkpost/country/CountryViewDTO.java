package io.nzbee.hkpost.country;

public class CountryViewDTO {

	private String ctyCode;
	
	private String hkpCtyCode;
	
	private String nameE;
	
	private String nameC;
	
	private String nameS;

	public String getCtyCode() {
		return ctyCode;
	}

	public void setCtyCode(String ctyCode) {
		this.ctyCode = ctyCode;
	}

	public String getHkpCtyCode() {
		return hkpCtyCode;
	}

	public void setHkpCtyCode(String hkpCtyCode) {
		this.hkpCtyCode = hkpCtyCode;
	}

	public String getNameE() {
		return nameE;
	}

	public void setNameE(String nameE) {
		this.nameE = nameE;
	}

	public String getNameC() {
		return nameC;
	}

	public void setNameC(String nameC) {
		this.nameC = nameC;
	}

	public String getNameS() {
		return nameS;
	}

	public void setNameS(String nameS) {
		this.nameS = nameS;
	}
}
